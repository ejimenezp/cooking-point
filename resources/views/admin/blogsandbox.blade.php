		<h3>Rias Baixas</h3>
		<p>Located in Galicia (northwest Spain), by the border with Portugal (across the river you’ll find the Vinho Verde region), this relatively new wine region produces white wines from <strong>Albariño</strong> grape, which gives young, refreshing wines with no aging at all. They are fragrant, intensely fruity, dry wines very appreciated by Spaniards.</p>
		<p>By the way, it is a common habit to serve them really cold, regardless the temperature outside, so don’t get surprised if the waiter puts the bottle in an ice bucket after opening the bottles.</p>
