		<h2>Wines from other regions</h2>
		<p>Above wines will be present in almost all decent eateries in Spain. Depending on the region you are visiting, you will also find other wines more available locally and that worth sampling. Here you are some examples:</p>
		<p><strong>Cava</strong>- Almost a religion in Catalonia, where the DO Penedes wine region is located this sparkling wine similar to Champagne, is enjoyed at any time of the day. As aperitive or during the meals (very good paella’s companion), or mixed with soda and juice to make “white sangria”.</p>
		<p><strong>Txacoli</strong>- This is the wine Basques drink when going out for the aperitive. It’s strongly Atlantic influenced and is sold young, slightly sparkling and low in alcohol. It is many times poured from high to release the fizz.</p>
		<p><strong>Elsewhere</strong>- I strongly recommend you trying local wines (there are wineries at any corner in Spain) that will give you a feeling about the land as well as the food eaten in such region.</p>
		<h2>Where to drink wine</h2>
		<p>Any time is a good time to drink good wine. Many bars have a chalkboard with the wines by glass, so it is a good option to try different types and regions. In restaurants, is quite usual hand out a wine list along with the menu, theoretically ordered by color, region and winery names.</p>
		<p>In case of doubt, you can always order “Un Rioja, por favor!”.</p>
