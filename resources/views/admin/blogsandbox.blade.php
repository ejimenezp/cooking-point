		<h3>Rioja</h3>
		<p>Sitting along the upper Ebro river, in the North, this is the oldest and well-known wine region in Spain. Its climate is not as rainy as the Basque Country, her neighbor in the North, and is not as extreme (in temps and drought) as central and southern regions.</p>
		<p>Rioja is famous for its red wines, from grape variety <strong>Tempranillo</strong>, which grows and ripens well on the clay and limestone slopes of the mountains that cover the northern area of this region. This variety is supplemented with <strong>Grenache</strong> and to lesser extend, Graciano, to give more body and smoothness to the wines.</p>
		<p>Rioja red wines are characterized by their barrel maturation, in 225-liter casks introduced by the French, and nowadays made also with American oak, that gives these wines a subtle vanilla flavor that is the classic Rioja’s taste.</p>
