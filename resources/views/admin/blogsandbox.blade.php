		<p>Spain’s regional diversity, in terms of climate, landscape, and ecosystems, is reflected also in our wines, which range from light, dry whites from the cool Atlantic region of Galicia (Northwest), to heavy, alcoholic reds in the Mediterranean and South. Additionally, Andalucia (South) is known for the production of fortified and dessert wines, the most famous of which is Sherry.</p>
		<p>Vines have been cultivated in Spain since between 4000 and 3000 BC. However, it is not until the 2nd century BC that Carthaginians grew wine in Spain during the Roman Empire. Since then, this precious liquid has been shipped to the at that time metropolis like Rome, England in the Middle Ages or France during the second half of 19th century when her vineyards were devastated by mildew and phylloxera plagues.</p>
		<p>Nowadays, Spain is the country in the world with the largest land devoted to vineyards, which bring us to the top 3 wine producers only with Italy and France ahead of us. By the way, the 2nd largest extension is in China, however, most of it is devoted to table grapes.</p>
		<p>If you are a wine lover, Spain is your country, because so far wine has a great value for money compared to our neighbors.</p>
		<h2>First thing, turn the bottle around</h2>
		<p>Spaniards many times set their expectations about the wine quality on its Denominación de Origen (DO, Designation of Origin), that talks about the production region, as well some other details about the harvest year, aging time and the grape varieties used to make it.</p>
		<p>This DO thing is like the French Appellation d'Origine Contrôlée (AOC) or the Denominazione di Origine Controllata (DOC) in Italy. Each DO have a standardized label or seal attached to each bottle produced under its rules. These labels are usually stuck at the back of the bottle.</p>
