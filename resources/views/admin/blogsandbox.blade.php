		<p>The state of alarm for COVID-19 ends on June 21 throughout Spain. The Spanish parliament has just approved the bill that sets the health & safety rules of the "new normality", name of the period until we have an effective therapy or vaccine against the virus:</p>
		<ul>
			<li><strong>Social distance of 1.5 m (5 feet)</strong> both indoors and outdoors (streets, shops, workplaces, accommodation, restaurants, museums, ...)</li>
			<li><strong>Mandatory use of the face mask</strong> if this 1.5m distance cannot be guaranteed all the time (i.e. strolling, shopping) or if other barriers are not suitable (sneezing guards, ...)</li>
			<li>Mandatory use of masks on public transport.</li>
		</ul>
		<p>In practice, that means you will have to wear a face mask almost every time you leave your home or hotel room.</p>
		<h2>How does that affect our classes?</h2>
		<p>As a business and workplace, we are committed to ensuring the safety of guests and employees against coronavirus spread, deploying authority requirements as follows:</p>
		<ul>
			<li>We reduce our class to <strong>6 people</strong> (50% of the usual capacity) to guarantee a social distance of <strong>6 feet</strong>. Your party will always be 6 feet away from the other guests, when cooking and eating. This way, there will not be physical barriers among workstations like sneeze guards or transparent partitions that might hamper class interaction.</li>
			<li>If you come alone, you don't pair up with anyone and cook individually.</li>
			<li>You are required to <strong>wear face mask during class</strong> except for when sit down to eat. Our staff will use it all the time.</li>
			<li>We <strong>clean and sanitize the entire facility</strong> before every class, especially restrooms and high-contact areas that are touched by guests and employees.</li>
			<li>You <strong>do not share any kitchen utensils</strong>, foodservice item, or condiment. Such articles are washed and sanitized before the class according to the regulation to avoid coronavirus spread.</li>
			<li>We provide <strong>hand sanitizer readily available</strong> for guests and employees.</li>
			<li>We provide individual disinfectant wipes available in restrooms.</li>
			<li>We <strong>reinforce room ventilation</strong> to increase the circulation of outdoor air as much as possible by keeping the kitchen hoods on.</li>
		</ul>
<h2>Things that does not change</h2>
<p>Fortunately, there are things that stay the same:</p>
<ul>
	<li>Private events are welcome. In fact, social gatherings of up to 20 people are allowed as long as they are reserved as a single group.</li>
	<li>Class format and schedule remains the same, and you keep cooking your own food.</li>
	<li>Market visit is still ongoing. Besides, smaller groups facilitate explanations and a deeper insight into Spanish produce and supply chain.</li>
</ul>


		<p>In any case, if there is anything that concerns you, please feel free to contact us.</p>
	
