		<h3>Rueda</h3>
		<p>This region sits hardly one hundred kilometers from Ribera del Duero downstream the Duero river. Despite they share soil and climate with most of the latter, this one is focused on producing young white wines from a local variety called <strong>Verdejo</strong>, which gives refreshing dry white wines with an herbaceous touch.</p>
		<p>By far, these wines are the most spread out around Spain, as they are relatively inexpensive and give excellent value for money.</p>
