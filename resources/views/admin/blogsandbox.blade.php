		<h3>Jerez - Sherry</h3>
		<p>This wine would deserve a whole blog, but I’ll make it short: don’t miss it if you are in Andalucia, where it is originally from. It’s been very appreciated along the history from middle ages to now.</p>
		<p>Sherry covers a range of fortified whites, ranging from the dry pale yellow like the “fino” and “manzanilla”, to the thicker, sweeter, full-bodied, amber colored ones like “oloroso”, “cream”, or “PX”.</p>
		<p>These superb wines are a conjunction of factors: 1) the mild Atlantic coast weather with sea breezes that alleviate the extremely hot of inland Andalucia; 2) the soil (called Albariza), a white chalky-looking one composed of limestone, clay, and sand which dries without caking, slowly releasing moisture to the vines during the growing season, 3) An aging and blending process called solera that results on blends of several year’s harvests.</p>
