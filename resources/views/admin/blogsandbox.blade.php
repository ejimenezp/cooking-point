		<h3>Ribera del Duero</h3>
		<p>This region is located in central-north Spain, along the upper valley of the Duero river, halfway between Madrid and the Basque Country. The climate there is far more extreme than in Rioja, with more sunny days and high-temperature variability in Summer.</p>
		<p>The predominant variety is also<strong> Tempranillo</strong> (here also called <strong>Tinto Fino</strong>), but the climate here makes the wines less acid, more alcoholic, with more body than Rioja’s ones. Aging is a common practice as well, but in this case, it is just around 6 months, therefore, can’t be labeled as “Crianza” but “Cosecha” (harvest, that is young wine).</p>
