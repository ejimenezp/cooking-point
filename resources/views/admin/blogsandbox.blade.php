		<h2>Oak aging, almost a must in red wines</h2>
		<p>Although there are little variations on the exact definitions among DOs, the red wine back labels include keywords to understand better the time a wine has been aging in oak casks and/or bottles. Thus, the different DOs rules specify the aging period for each officially recognized category of wine:</p>
		<p><strong>Cosecha</strong> – Young wine released the following year after harvest, with no cask aging or perhaps just a short period of 3-6 months.</p>
		<p><strong>Crianza</strong> – wines released on its third year after harvest, aged 12 months in oak cask and 12 in bottle afterward. For example, that means that the youngest ones you will drink as of Summer 2019 are from 2015.</p>
		<p><strong>Reserva</strong> – released on the fourth year after harvest, aged a total of 3 years between barrel and bottled, with a minimum of 12 months in a barrel.</p>
		<p><strong>Gran reserva</strong> – released on the fifth year after harvest, aged a minimum of 2 years in cask and 3 in bottles.</p>
		<p>For example, the Rioja bestsellers are usually Crianza, when the oak flavor is well balanced with the fruity primary aromas. Conversely, most of the white wines you will find are young, dry ones (except for Sherry wines, that deserve a whole new chapter).</p>
