<div class="header2">A Little Bit of History</div>
<p></p>

<p>Till recently, Navarra was thought to be the cradle of this Spanish delicacy. The first testimony of this recipe appears mentioned in a document submitted in 1817 to the Courts of Navarra, where it is said that two or three eggs omelets were enough to feed five or six men as women cooked them with fried potatoes.</p>

<p>Legend has it that General Tomás Zumalacárregui visited a tavern to have lunch and the landlady did not have enough eggs to prepare an omelet due to shortage war periods, and she got creative cooking the few eggs she had with fried potatoes.</p>

<p>Nevertheless, recent discoveries suggest that Villanueva de la Serena (Extremadura, Southwest Spain) was, in fact the place where this delicacy was born in 1789. The invention of the dish is credited to two nobles determined to tackle famines, so recurrent at that time in Spain. Their goal was to create a cheap alternative to bread replacing part of the flour with potatoes. The dough would then be shaped in rounded slices (“tortitas”) and fried in oil. Eggs seemed to be added to the recipe shortly after because they were already mentioned as a natural addition to this mix.</p>

<div class="header2">Recipe</div>
<p></p>

<p>But what are the exact ingredients of Spanish Omelet? If you are a foodie (which is more than likely if you’ve got to this blog) you are more than likely to have done some online research which has probably led you to a great variety of versions, such as Jamie Olivier’s which included chorizo or peppers. But the truth is the genuine recipe includes only potatoes, eggs and optionally, onions. This last ingredient has proved to be the most controversial of the components of Spanish omelet, for there are as many supporters as detractors of this version.</p>

<img class="img-fluid center-block" alt="how to flip an omelet" src="/images/blog/flipping-omelet.jpg" />

<p>Ingredients:</p>
<ul>
	<li>350 gr potato</li>
	<li>125 gr (eq. 1/2) onion</li>
	<li>3 large eggs</li>
	<li>Olive oil</li>
	<li>Salt</li>
</ul>
<p>Directions:</p>
<ol>
	<li>Peel and chop the potatoes into thin slices.</li>
	<li>Fry them in olive oil at very low temperature. Reserve.</li>
	<li>Slice the onion into strips and fry until translucent.</li>
	<li>In a large bowl, whisk the eggs with a good pinch of salt, add the drained potatoes and onion.</li>
	<li>Coat a clean frying pan with olive oil and add the egg mix and cook over a low heat.</li>
	<li>Once the bottom is firm, flip the omelet with a plate to cook the other side.</li>
	<li>Allow to cool before serving.</li>
</ol>

