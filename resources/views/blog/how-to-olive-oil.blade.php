<div class="header2">Cooking with different olive oils</div>

<p>Always keep on hand a bottle of basic, <strong>inexpensive extra virgin olive oil</strong> for cooking. As a rule, use extra virgin olive oil for any method of cooking except for deep frying (sautéing, simmering, shallow frying, roasting...).</p>

<p>When you are deep frying, use a <strong>plain olive oil</strong> but don’t worry about the heat or the smoke point – it is far above the usual home cooking temperatures for all kinds of olive oil (extra virgin or plain). So don't stop using it because of that reason, as you will never reach those temperatures in your daily cooking.</p>

<p>Use a more expensive finer, <strong>higher-quality extra virgin olive oil</strong> for raw recipes or dressings. It is worth spending more money on an oil that will make the difference in your dishes.</p>

<div class="header2">Buying olive oil</div>

<p>Don’t be driven by claims like “first press”, “cold press”, “low acidity”, “unrefined” … because all extra virgin olive oils are, by law, extracted the same way. Don’t be concerned with “non-GMO certified” either, because genetically modified olives are not allowed in Europe (where most olive oil comes from).</p>

<p>When it comes to buying a nice, first-class olive oil, we recommend oils from a single, unambiguous source, so look for packaging that includes phrases like “Product of Spain” or “Produced and bottled in California”...</p>

<p>Avoid brands that mention several countries on their bottles. Some European brands add extra guarantee of origin as they adhere to the European Union’s <a href="/protected-designation-of-origin">Protected Designation of Origin</a> law that guarantees the product is associated to a certain region, produce varieties, production rules and quality standards.</p>

<p>Choose oils from small producers (i.e. “estate bottled”) instead of big trading or retail corporations. As with wine, you can trust small wineries more than huge corporations.
Choose the brands that mention the <strong>olive varieties</strong> used. Again, this is like with wine - can you imagine buying a wine whose label read “very good red wine”? Once you are familiar with varieties, you can expect the same flavors and aromas regardless of the brand. Some varieties are more aromatic (so, more appropriate for salads) and others may add more character to your recipes.

<p>The most common olive varieties in Spain are (ordered from more aromatic to more pungent):</p>


<dl>
	
	<dd><strong>Arbequina: </strong>Oils range from green to yellow in color, with fruity, sweet aromas reminiscent of apples and fresh almonds. The most representative variety of Catalonia. Best choice for raw recipes.</dd>
	<p></p>
	<dd><strong>Picual: </strong>Very steady in time, with personality and body. It is fruity and robustly bitter with clear peppery overtones. The #1 in production volume, mostly from Jaen, the Spanish province with the highest concentration of olive trees in the world. Good for both raw recipes and cooking.</dd>
	<p></p>
	<dd><strong>Hojiblanca: </strong>The oil is deep green in color, with aromas of ripe fruit and reminiscent of avocado, with a pleasant taste with a slight touch of bitterness and pungency. From mid-west Andalusia. Good to add character to your cooking.</dd> 
	<p></p>
	<dd><strong>Cornicabra: </strong>The oil is greenish-yellow to golden in color, with a fresh aroma and taste ranging between sweet, bitter and slightly pungent. From central Spain. Good for cooking (frying).</dd>
