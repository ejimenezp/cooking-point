<div class="header2">Storing olive oil</div>

<p>Dark bottles or tins are preferred as they protect the oil from light. In all cases, keep them in the dark, such as inside a cupboard, and away from heat sources.</p> 

<p>There is no need to refrigerate the oil, and be mindful that it gets solid below 10 ºC (50º F).  Don’t worry though, if your oil turns solid it will not impact the taste or quality.</p> 

<p>Once you open a bottle, use it - don’t keep it as a precious treasure. As with any juice, it will lose properties as time goes by.</p>
 



