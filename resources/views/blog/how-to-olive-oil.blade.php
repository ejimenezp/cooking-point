<div class="header2">Spain - the olive oil capital of the world</div>

<p>Spain is the world’s top producer and exporter of olive oil and table olives. Although it varies from season to season, Spain produces 45%-50% of the world’s olive oil production, followed by Italy (10%-15%) and Greece (8%-10%).</p> 

<p>The olive tree is native to the Mediterranean, and is cultivated over a vast area in Spain. Olive growing and associated products are one of the top sectors in the Spanish food industry in terms of economic and social importance.</p>

<div class="header2">Understanding olive oil – the basics</div>

<p>It’s important to remember that <strong>olive oil is the juice of a fruit</strong>. As with all fruits and juices, the quality of the fruit and the process of making the juice can impact on flavor, use and storage.</p>

<p>The classification of “extra virgin” or “virgin” comes down to the quality of the juice. Extra virgin olive oil is obtained directly from olives and solely by mechanical means. It is purer than any other oil, with no chemical solvents, refining process or blending.</p>

<p>Virgin olive oil is made the same way, but doesn’t meet the standards of superior quality associated with extra virgin. Tasting panels have strict tests and a points system to ensure the label “extra” is associated only with the very best oils.</p>

<p>Plain olive oil is made by mixing refined olive oil (85%) with extra virgin olive oil (15%).</p>


