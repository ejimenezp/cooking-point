<p>As we’ve already learned on our post <a href="/how-to-use-olive-oil">How to use, buy and store olive oil</a>, olive oil should be thought of as the juice from a fruit. As with any other fruit, there needs to be a harvest as well as a process for extracting the juice from the fruit.</p> 

<p>As a general rule, quality olive oils are harvested, extracted and stored on the same day. This means that during the harvest season, usually between November and March, olive oil is produced every day.</p>


<div class="header2">Step 1 – the olive harvest</div>

<p>Firstly, a big mat is laid out under the tree to catch the olives. Workers then hit the branches with long poles to shake the fruit from the trees, or some use special heavy machinery that vibrates along the trees’ branches.</p>

<p>Once the olives and leaves have fallen onto the mat, they are collected and carried to the oil mill by tractor.</p>

