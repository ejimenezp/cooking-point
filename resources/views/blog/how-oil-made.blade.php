<p>This physical process of crushing, mixing and decanting impacts the temperature of the paste, which then triggers chemical reactions (oxidation). The oxidation must be controlled to ensure a higher quality oil – the term “cold press” comes from the fact that the temperature is kept below 25º C during the mixing process.</p>

<p>And then you have a fresh, fragrant olive juice that is almost ready to be enjoyed.</p> 

<p>Just left is the filtering and storing of the olive oil in stainless steel vats, very much like the ones used in wineries.</p>

