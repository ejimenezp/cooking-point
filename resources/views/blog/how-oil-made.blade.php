<div class="header2">Step 3 - testing and analysis</div>


<p>Once the oil has been extracted from the olives, it needs to be categorized. This involves two steps – a lab analysis and a sensory analysis. The lab analysis looks for the presence of chemical compounds in the oil, while the sensory analysis checks other features such as smell and taste.</p>

<p>The most important indicator resulting from the chemical test is acidity, which is measured in degrees and equates to the percentage of free fatty acids in the oil. The lower the acidity, the better and for this reason “extra virgin” olive oil can’t exceed 0.8º acidity (0.8%). For the sensory test, the extra virgin olive oil must not have any defect in taste or aroma.</p>

<p>If the acidity is between 0.8º and 2º or the oil has little sensory defects, the oil will be categorized as “virgin” olive oil. That is, it is still a nice olive oil, but not as good as the extra virgin.</p>

<p>An oil with acidity higher than 2º or with severe taste defects will need to be refined to remove all defects – whether they are chemical or sensory. The result of the refining process is a liquid without color, smell or taste. It is just plain transparent fat in liquid form. This fat is blended with a small portion (10-20%) of virgin olive oil to recover the color, taste and aroma, which is then called plain “olive oil”.</p> 

