<div class="header2">Step 2 - extracting the oil</div>

<p>Once in the mill, the olives are washed to remove any dust or dirt from the harvest, collection and transportation.</p> 

<p>Once washed, the olives are separated from the stems and leaves. This process is manually supervised to ensure that no stones or rotten olives get into the mix.</p>

<p>Next, the olives are crushed in a hammer mill to produce smaller pieces of olive. These then go into a mixer to produce a paste which is then pumped into a decanter.</p> 

<p>Finally, in the decanter the process of centrifugation is used to separate the oil from water and solid residue.</p>



