<div class="header2">How to choose a good Iberian ham</div>
<p></p>

<p>Nowadays, only 15% of Iberian hams come from acorn fed pigs (black or red tag), whereas the most common is the plain Iberian ham (65%, white tag). When choosing a ham in a shop or restaurant, you should pay attention to the color of the tag or the description, moreover if you are looking for the genuine acorn fed 100% Iberian ham.</p>

<p>Sometimes terms like "reserva" (reserve) or "great reserva" (grand reserve) allude to the aging of the ham. However, this terminology is not regulated. The law requires a minimum of 2 years for hams over 7 kg, although it is normal to age for 3+ years, as it really depends on the size and amount of fat that the piece has.
I recommend buying only acorn fed Iberian ham (black or red label), where the complexity of flavors and the high content of unsaturated fats is better appreciated.</p>

<p>I hope this post serves to introduce you to the world of Spanish ham (and particularly the Iberian ham), and appreciate the dietary and culinary benefits of it. You can see practical examples in the visits to the market as part of our paella classes every morning. </p>


