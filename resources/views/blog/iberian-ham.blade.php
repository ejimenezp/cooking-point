<div class="header2">How To Choose A Good Iberian Ham</div>
<p></p>

<p>Nowadays, only 15% of Iberian hams come from acorn fed pigs (black or red tag), whereas the most common is the plain Iberian ham (65%, white tag). When choosing a ham in a shop or restaurant, you should pay attention to the color of the tag or the description, moreover if you are looking for the genuine acorn fed 100% Iberian ham.</p>

<p>Sometimes terms like "reserva" (reserve) or "great reserva" (grand reserve) allude to the aging of the ham. However, this terminology is not regulated. The law requires a minimum of 2 years for hams over 7 kg, although it is normal to age for 3+ years, as it really depends on the weight and amount of fat that the piece has.</p>

<p>I recommend buying only acorn fed Iberian ham, where the complexity of flavors and high content of unsaturated fats are better appreciated.</p>

