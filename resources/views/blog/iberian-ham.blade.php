		<h1 class="header1">What is Iberian Ham</h1>

<img class="img-responsive center-block" alt="iberian pig" src="/images/blog/iberian-pig.jpg" />

<p></p>

<p>One of the foods most appreciated by people visiting Spain is the "jamón", that is, the leg of the pork once it is salted and dried. After the surprise of seeing them hanging in our bars and grocery stores, everyone surrenders to the genuine taste of this dry and slightly salty meat. It is usually eaten as a tapa or in a sandwich, and it is also present in many traditional dishes.</p>

<p>To choose a good ham, the first thing you should know is that in Spain there are two types of pigs that produce this delicacy:</p>

<ul>
<li><strong>White pig</strong>, of a breed called Duroc, which is the usual pig in most countries and is raised on farms, indoors with compound feed.</li>
<li><strong>Iberian pig</strong>, which is a breed only raised in Spain and Portugal (there is called 'porco Alentejano'), and has unique characteristics because of the way they are bred.</li>
</ul>

<p>From the gastronomic viewpoint, it is said that an acorn fed Iberian ham is comparable to a good old wine. Experts claim to find innumerable flavors, mainly in the fat layer, which tastes very nutty and grassy.</p>

<p>By the way, we only call 'jamon' the back leg of the animal. The front leg (shoulder) is called ‘paletilla’. This is smaller, and usually has more fat and is more salty, but a good value for money.</p>

<div class="header2">The ‘dehesa’, landscape key for pig breeding</div>
<p></p>

<p>Traditionally, Iberian pigs were raised in ‘dehesas’, pasture areas typical of the Iberian Peninsula and consisting of Mediterranean forest with predominance of trees of the variety Quercus (holm oaks, cork oaks ...). These forests have been maintained and exploited by the man during centuries for the breeding of cattle, of which Iberian pig and fighting bull stand out. The dehesas are located mainly along the border of Spain and Portugal from Salamanca to Huelva.</p>

<p>For generations, the Iberian pig has been raised in these meadows fed exclusively from the acorns and pastures found in these spots. This diet has made their fat have high presence of fat good for your heart, particularly oleic acid, which amounts to 60%. Diets rich in oleic acid (only olive oil contains more than Iberian ham) help lower total cholesterol and bad cholesterol (LDL) and raise good cholesterol (HDL) and protects against cardiovascular disease.</p>

<img class="img-responsive center-block" alt="iberian pigs in a dehesa" src="/images/blog/dehesa.jpg" />

<p>As the feeding of these animals is key, the Ministry of Agriculture maintains a record of the existing dehesas in Spain and controls the number of heads that can be raised in each one according to the density of trees. To ensure that pigs only gain weight by eating acorns, a limit of 1.25 heads / hectare of pasture (1 head per 2 acres) is established.</p>

<p>In recent years, Iberian ham has become very popular thanks to the fact that it has been possible to raise these animals indoors and breed with white pigs, which are more productive (bigger and heavier hams). Obviously, this has resulted in lower prices in exchange for loss of taste and dietary properties.</p>

<p>The Ministry of Agriculture maintains a ‘Genealogy Book’, which registers all Iberian cattle to trace and guarantee breed purity and avoid inbreeding.</p>
 
<div class="header2">Iberian Ham’s Classification</div>
<p></p>
<p>In the market, you can find a range ham categories based on their breed and the way they are farmed. To avoid misunderstandings when purchasing and to facilitate their identification, the law requires the use of a code names and colors in stores and restaurants. In stores, hams must have a plastic tag on the hoof whose color to determine their type. In bars and restaurants, the denomination must be unequivocal.</p>

<p>Depending on breed purity and food, we found several types of hams:</p>

<ul>
<li><strong>Jamón de bellota 100% ibérico (Acorn fed 100% Iberian ham)</strong>: from pigs 100% pure Iberian breed, raised in freedom in ‘dehesas’, fed exclusively with what grows in these pastures, basically acorns. Plastic label color: black. Keywords: "100% iberian", "pata negra" (black hoof).</li>

<li><strong>Jamón de bellota ibérico (Acorn fed Iberian ham)</strong>: from pigs 50% Iberian (mother 100% Iberian, father white pig), raised in freedom on the 'dehesa' also. This variety exists because of the difficulty of maintaining 100% purebred pigs over time. Plastic label color: red. Keywords: "bellota" (acorn).</li>

<li><strong>Jamón de cebo de campo ibérico (Free-range Iberian ham)</strong>: from pigs 50% Iberian, raised on a free range (not necessarily a ‘dehesa’), but fed with cereals and legumes. It is a way to exploit more intensively the grazzing land that do not meet the 'dehesa' requirements (density of up to 10 heads / hectare = 4 heads / acre). Plastic tag color: green. Keywords: “cebo de campo” (free range).</li>

<li><strong>Jamón de cebo ibérico or simply jamón ibérico (Farm raised Iberian ham or Iberian ham)</strong>: from pigs 50% Iberian, raised indoors, fed like normal white pigs. These pigs keep Iberian gastronomic properties and a reasonable high unsaturated fats concentration to be worth picking them against white pig ham. Plastic label color: white. Keywords: "iberico" (iberian).</li>
</ul>

<p>We leave the definition of <strong>Jamón Serrano</strong> as the one from white pigs, raised indoors on farms. </p>

